CREATE TABLE users (
    id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    email VARCHAR (255) UNIQUE NOT NULL,
    password VARCHAR (225) NOT NULL
);

CREATE TABLE workouts (
    id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    user_id INTEGER
        REFERENCES users(id) ON DELETE CASCADE NOT NULL,
    type VARCHAR (255) NOT NULL,
    difficulty VARCHAR (255) NOT NULL
);

CREATE TABLE exercises (
    id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    workout_id INTEGER
        REFERENCES workouts(id) ON DELETE CASCADE NOT NULL,
    name VARCHAR (255) NOT NULL,
    image VARCHAR (255) NOT NULL,
    description VARCHAR (255) NOT NULL,
    sets VARCHAR (255) NOT NULL,
    rest VARCHAR (255) NOT NULL,
    is_upper INTEGER DEFAULT 0 NOT NULL,
    is_lower INTEGER DEFAULT 0 NOT NULL,
    is_beginner INTEGER DEFAULT 0 NOT NULL,
    is_intermediate INTEGER DEFAULT 0 NOT NULL,
    is_advanced INTEGER DEFAULT 0 NOT NULL
);